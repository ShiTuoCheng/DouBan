public class CastModel {

    private String cast_alt;
    private String cast_avatar;
    private int cast_id;
    private String cast_name;

    public String getCast_alt() {
        return cast_alt;
    }

    public void setCast_alt(String cast_alt) {
        this.cast_alt = cast_alt;
    }

    public String getCast_avatar() {
        return cast_avatar;
    }

    public void setCast_avatar(String cast_avatar) {
        this.cast_avatar = cast_avatar;
    }

    public int getCast_id() {
        return cast_id;
    }

    public void setCast_id(int cast_id) {
        this.cast_id = cast_id;
    }

    public String getCast_name() {
        return cast_name;
    }

    public void setCast_name(String cast_name) {
        this.cast_name = cast_name;
    }
}
